public class Animal {

    String name;

    String race;

    String color;

    String gender;


    public Animal(String name, String race, String color, String gender) {
        this.name = name;
        this.race = race;
        this.color = color;
        this.gender = gender;
    }

    public Animal(String name, String race, String color) {
        this.name = name;
        this.race = race;
        this.color = color;
    }
}
